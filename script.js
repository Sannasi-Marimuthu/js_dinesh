// string


myVariable = "Web Developer"

console.log(myVariable)

console.log(typeof myVariable)

// length - starts from 1
console.log(myVariable.length)

// charAt -starts from 0
console.log(myVariable.charAt(4)) 

//index of - starts from 0
console.log(myVariable.indexOf("e"))

//last index of - starts from 0
console.log(myVariable.lastIndexOf("e"))

//slice
console.log(myVariable.slice(0,3) )

//upperCase - Capital letters
console.log(myVariable.toUpperCase())


//lowerCase - small letters
console.log( myVariable.toLowerCase())

// include
console.log( myVariable.includes("san") )

// split
console.log( myVariable.split(" ") )

//replace
console.log( myVariable.replace("Web", "Frontend") )